class SilverbachConjecture {
   public:
      vector <int> solve(int n) {
         vector < int > ans;
         int first=4;
         int second=n-4;
         while((second&1)&&(second%3)){
            second-=2;
            first+=2;
         }
         ans.push_back(first);
         ans.push_back(second);
         return ans;
      }
};


