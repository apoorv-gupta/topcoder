class GameOfStones {
public:
	int count(vector <int> stones) {
           int even=0,odd=0;
           int sum =0;
           for(vector<int>::iterator it = (stones).begin(); it != (stones).end(); it++){
              if((*it)&1) odd++;
              else even++;
              sum+=(*it);
           }
           int n = stones.size();
           int tar = sum/stones.size();
           if((n*tar )!=sum) return -1;
           if(even&&odd) return -1;
           if((tar&1)!=(stones[0]&1)) return -1;
           int ans=0;
           for(vector<int>::iterator it = (stones).begin(); it != (stones).end(); it++){
              if((*it)<tar) continue;
              ans+=((*it)-tar)/2;
           }
           return ans;
	}
};


