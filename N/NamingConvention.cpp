class NamingConvention {
public:
	string toCamelCase(string v) {
           string res;
           for(int i=0;i<v.size();++i){
              if(v[i]=='_'){
                 res+=toupper(v[i+1]);
                 i++;
              }
              else
              res+=v[i];
           }
           return res;
	}
};


