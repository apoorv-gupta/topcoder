class BuildingHeightsEasy {
public:
	int minimum(int M, vector <int> h) {
           int n = h.size();
           vector < int > diff = vector < int >(n+2);
           diff[0]=25010;
           diff[n+1]=25010;
           sort(h.begin(),h.end());
           int ans=25010;
           for(int res=1;res<=50;++res){
              int lt=0,rt=0;
              for(int i=1;i<=n;++i){
                 diff[i]=abs(h[i-1]-res);
                 if(h[i-1]>res) diff[i]+=25010;
                 if(diff[i]<diff[lt])
                    lt=i;
              }
              rt=lt+1;
              int lans=0;
              int rem=M;
              while(rem--){
                 if(diff[lt]<diff[rt]){
                    lans+=diff[lt];
                    lt--;
                 }
                 else{
                    lans+=diff[rt];
                    rt++;
                 }
              }
              if(lans<ans)
                 ans=lans;
           }
           return ans;
	}
};


